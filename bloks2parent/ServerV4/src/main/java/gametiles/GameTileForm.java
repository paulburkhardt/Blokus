  private final ArrayList<Coordinate> coordinates = new ArrayList<>();
  private final ArrayList<ArrayList<Coordinate>> trigonStates = new ArrayList<>();

  public ArrayList<Coordinate> getCoordinates() {
    return coordinates;
  }

  public ArrayList<ArrayList<Coordinate>> getTrigonStates() {
    return trigonStates;
  }

  /**
   * Constructor for GameTileForm when not Trigon.
   *
   *  @param coordinates contains the coordinates of the GameTile in its standard form
   */
  GameTileForm(int[][] coordinates) {
    for (int[] i : coordinates) {
      this.coordinates.add(new Coordinate(i[0], i[1]));
    }
  }

  /**
   * Constructor for GameTileForm when Trigon.
   *
   * @author pburkhar
   * @param states is an Array in which all States of the TrigonGameTile are stored in form of
   *     Arrays Containing the Coordinates of the Tiles as another array
   */
  GameTileForm(int[][][] states) {
    int counter = 0;
    for (int[][] coords : states) {
      ArrayList<Coordinate> temp = new ArrayList();
      for (int[] i : coords) {
        if (counter == 0) {
          this.coordinates.add(new Coordinate(i[0], i[1]));
        }
        temp.add(new Coordinate(i[0], i[1]));
      }
      counter++;
      this.trigonStates.add(temp);
    }
  }
